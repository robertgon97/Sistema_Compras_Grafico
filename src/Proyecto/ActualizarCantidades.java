    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(isNumeric(jTextField1.getText().toString())){
            if(acc_id!=-1){
                ActualizarInventario su = new ActualizarInventario();
                su.update(id,Integer.parseInt(jTextField1.getText().toString()),acc_id);
                sp.retreve();
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"No esta permitido actualizar");
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Añadido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

